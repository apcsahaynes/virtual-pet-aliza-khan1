public class Cleanliness {
    private String name;
    public int cleanlinessIncrease;
    
  public Cleanliness(String name, int cleanlinessIncrease){
    this.name = name;
    this.cleanlinessIncrease = cleanlinessIncrease;
  }
  
  public String getName(){
    return name;
  }
  
  public int getcleanlinessIncrease(){
    return cleanlinessIncrease;
  }
    
    
    
}
